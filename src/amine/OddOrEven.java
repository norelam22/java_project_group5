package amine;

public class OddOrEven {
    public static void main(String[] args){
        int num1 = 5;
        int num2 = 6;
        if (num1%2==0){
            System.out.println(num1 + " -> Even");
        }else{
            System.out.println(num1 + " -> Odd");
        }
        if(num2%2==0){
            System.out.println(num2 +" -> Even");
        }else{
            System.out.println(num2+ " -> Odd");
        }
    }
}
