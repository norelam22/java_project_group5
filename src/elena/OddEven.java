public class OddEven {
    public static void main(String[] args) {
        OddEven(5);
        OddEven(6);

    }
        public static void OddEven (int num) {
            if (num % 2 == 0) {
                System.out.println(num + " is even number");
            } else {
                System.out.println(num + " is odd number");
            }

        }
    }

